
public class Exam4 {
	public static void main(String[] args) {		
		int num = 0;
		System.out.println("num = " + num);
		
		// 변수값 1 증가
		num = num + 1;
		System.out.println("num = " + num);
		
		num += 1;
		System.out.println("num = " + num);
		
		num++;
		System.out.println("num = " + num);
		
		++num;
		System.out.println("num = " + num);
		System.out.println("------------");
		
		// 변수값 1 감소
		num = num - 1;
		System.out.println("num = " + num);
		
		num -= 1;
		System.out.println("num = " + num);
		
		num--;
		System.out.println("num = " + num);
		
		--num;
		System.out.println("num = " + num);
	}
}











