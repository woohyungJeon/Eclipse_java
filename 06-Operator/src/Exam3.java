
public class Exam3 {
	public static void main(String[] args) {
		int a = 100;
		
		a += 100;	//a = a + 100;
		System.out.println(a);
		
		a -= 100;	//a = a - 100;
		System.out.println(a);
		
		a *= 2;		//a = a * 2;
		System.out.println(a);
		
		a /= 5;		//a = a / 5;
		System.out.println(a);
		
		a %= 5;		//a = a % 5;
		System.out.println(a);
	}
}
